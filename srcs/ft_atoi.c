#include "libft.h"

int		ft_atoi(const char *s)
{
	int	sign;
	int	nbr;
	int	i;

	sign = 1;
	nbr = 0;
	i = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v' || s[i] == '\f'
			|| s[i] == '\r' || s[i] == '\n')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		nbr = nbr * 10 + ((int)s[i] - 48);
		i++;
	}
	return ((int)nbr * sign);
}
