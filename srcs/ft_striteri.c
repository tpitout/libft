#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *s))
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	if (s)
		while (s[i] != '\0')
		{
			f(i, s + i);
			i++;
		}
}
