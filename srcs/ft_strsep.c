#include "libft.h"

char	*ft_strsep(char *str, int c)
{
	int		i;

	if (!str || !c)
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		str[i] = (unsigned char)c;
		i++;
	}
	return (str);
}
